package alm;

/**
 * ALM Exception 
 *       
 *  
 */
public class ALMException extends Exception {
    public ALMException() {
        super();
    }
    public ALMException(String message) {
        super(message);
    }
    public ALMException(String message, Throwable cause) {
        super(message, cause);
    }
}
 